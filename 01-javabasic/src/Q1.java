//Q1.java 클래스 파일을 생성
public class Q1 {
	//main메서드를 내부에 생성
	public static void main(String[] args) {
		//main메서드 내부에 정수 변수를 하나 선언
		int a;
		//main메서트 내부에 정수 변수 두개를 한 줄에 선언
		int b, c;
		//위 세개의 변수에 원하는 값들을 초기화
		a = 14;
		b = 28;
		c = 57;
		//System.out.println()을 이용해 변수 3개의 합을 출력
		System.out.println(a + b + c);
	}
}
